// Backdrop ferne Ebene (Parallax) – kleine Silhouette für Tiefe.
export function getBackdrop(theme) {
  return rasterize(`backdrop_${theme.backdrop}`, () => {
    const w = 120, h = 30;
    const rows = [];
    if (theme.backdrop === 'building') {
      // Schulgebäude-Silhouette mit ein paar Fenstern.
      for (let y = 0; y < h; y++) {
        let r = '';
        for (let x = 0; x < w; x++) {
          // Drei Häuser hintereinander: 20-50 niedrig, 55-85 hoch, 90-115 niedrig.
          const inA = x >= 20 && x < 50 && y >= 14;
          const inB = x >= 55 && x < 85 && y >= 8;
          const inC = x >= 90 && x < 115 && y >= 16;
          if (inA || inB || inC) {
            // Fenster
            if ((x % 5 === 2) && ((y % 6 === 0) || (y % 6 === 3))) r += 'Y';
            else r += 'r';
          } else r += '_';
        }
        rows.push(r);
      }
    } else if (theme.backdrop === 'blackboard') {
      // Tafel + ein paar Schultafel-Linien.
      for (let y = 0; y < h; y++) {
        let r = '';
        for (let x = 0; x < w; x++) {
          if (y >= 5 && y < 25 && x >= 20 && x < 100) {
            if ((y === 10 || y === 14 || y === 18) && (x % 3 !== 0)) r += 'W';
            else r += 'g';
          } else r += '_';
        }
        rows.push(r);
      }
    } else {
      // Sprossenwand-Schatten.
      for (let y = 0; y < h; y++) {
        let r = '';
        for (let x = 0; x < w; x++) {
          if ((x === 25 || x === 50 || x === 75 || x === 100) && y < 28) r += 'n';
          else if ((y === 5 || y === 12 || y === 19 || y === 26) && x > 22 && x < 103) r += 'n';
          else r += '_';
        }
        rows.push(r);
      }
    }
    return rows;
  });
}

