// Programmatic pixel sprites. Each sprite is a 2D array of palette indices,
// rendered via small offscreen canvases that can be drawn at any scale.

const PALETTE = {
  _: null,                  // transparent
  K: '#0a0c1e', 'k': '#1b1f3a',
  W: '#f1f1f1', 'w': '#c8d2dc',
  S: '#f5d6a8',  // skin
  s: '#d8a87a',  // skin shadow
  H: '#3a2820',  // hair dark
  h: '#a06030',  // hair brown
  Y: '#ffd54a',  // hair blond / accent
  B: '#3a5fb0',  // blue (shirt)
  b: '#27447a',
  R: '#ef5b5b',
  r: '#8a2e2e',
  G: '#4caf50',
  g: '#2f7a33',
  O: '#ff8a3a',  // orange
  P: '#a05bcf',  // purple
  p: '#6f3aa0',
  C: '#4ad6ff',  // cyan
  c: '#2a8aa8',
  Z: '#b8941f',  // dark yellow
  N: '#7a5a3a',  // brown
  n: '#4e3722'
};

const cache = new Map();

function compile(grid, paletteOverride = {}) {
  const pal = { ...PALETTE, ...paletteOverride };
  const rows = grid;
  const h = rows.length;
  const w = rows[0].length;
  const c = document.createElement('canvas');
  c.width = w; c.height = h;
  const ctx = c.getContext('2d');
  const img = ctx.createImageData(w, h);
  for (let y = 0; y < h; y++) {
    const row = rows[y];
    for (let x = 0; x < w; x++) {
      const key = row[x];
      const color = pal[key];
      const i = (y * w + x) * 4;
      if (!color) { img.data[i + 3] = 0; continue; }
      const rgb = parseColor(color);
      img.data[i] = rgb[0];
      img.data[i + 1] = rgb[1];
      img.data[i + 2] = rgb[2];
      img.data[i + 3] = 255;
    }
  }
  ctx.putImageData(img, 0, 0);
  return c;
}

function parseColor(c) {
  if (c[0] === '#') {
    return [parseInt(c.slice(1, 3), 16), parseInt(c.slice(3, 5), 16), parseInt(c.slice(5, 7), 16)];
  }
  return [255, 0, 255];
}

function rasterize(name, builder, paletteOverride = {}) {
  const key = name + JSON.stringify(paletteOverride);
  if (cache.has(key)) return cache.get(key);
  const c = compile(builder(), paletteOverride);
  cache.set(key, c);
  return c;
}

